def soma(numero_1, numero_2):
    soma = numero_1 + numero_2
    mensagem = f"resultado da soma é: {soma}" 
    return mensagem

def subtracao(numero_1, numero_2):
    soma = numero_1 - numero_2
    mensagem = f"resultado da subtracao é: {soma}" 
    return mensagem

def divisao(numero_1, numero_2):
    soma = numero_1 / numero_2
    mensagem = f"resultado da divisão é: {soma}" 
    return mensagem

def multiplicacao(numero_1, numero_2):
    soma = numero_1 * numero_2
    mensagem = f"resultado da multiplicação é: {soma}" 
    return mensagem